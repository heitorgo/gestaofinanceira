    private void jtPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtPesquisaKeyReleased
        try {
            DefaultTableModel mp = (DefaultTableModel)jtbDados.getModel();
            lista = DAO.pesquisar(jtPesquisa.getText());
            mp.setNumRows(0);
            for(ClienteModel ob: lista){
                mp.addRow(new String[]{""+ob.getIdcliente(),ob.getNomecliente(),ob.getCpfcliente(),ob.getDatanasccliente()});
            }
            jbCancelar1.setEnabled(true);
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro: "+e.getMessage());
        }
    }//GEN-LAST:event_jtPesquisaKeyReleased

    private void jtbDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbDadosMouseClicked
        try {
            int linha = jtbDados.getSelectedRow();
            if(linha>-1){
                jlIdcliente1.setText(jtbDados.getValueAt(linha, 0).toString());
                jtDatanasccliente1.setText(jtbDados.getValueAt(linha, 3).toString());
                jtCidadecliente1.setText(""+lista.get(linha).getCidadecliente());
                jtNumcasacliente1.setText(""+lista.get(linha).getNumcasacliente());
                jtComplcasacliente1.setText(""+lista.get(linha).getComplcliente());
                jtContatocliente1.setText(""+lista.get(linha).getContatocliente());
                jtNomecliente1.setText(jtbDados.getValueAt(linha, 1).toString());
                jtCpfcliente1.setText(jtbDados.getValueAt(linha, 2).toString());
                jtEmailcliente1.setText(""+lista.get(linha).getEmailcliente());
                jtRuacliente1.setText(""+lista.get(linha).getRuacliente());
                for(int i=0;i<listaEmpresa.size();i++){
                    if(listaEmpresa.get(i).getIdempresa() == lista.get(linha).getEmpresa().getIdempresa()){
                        jcbEmpresa.setSelectedIndex(i);
                    }
                }
                jbEditar.setEnabled(true);
                jbExcluir.setEnabled(true);
                jbCancelar1.setEnabled(true);
            } else {
                jbEditar.setEnabled(false);
                jbExcluir.setEnabled(false);
                jbCancelar1.setEnabled(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao selecionar o registro: "+e.getMessage());
        }
    }//GEN-LAST:event_jtbDadosMouseClicked

