    private boolean validarCampos(){
        if(jtDatadespesa.getText().trim().length()<7 ){
            JOptionPane.showMessageDialog(null, "Caractere data Insuficiente(<7)", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(jtValordespesa.getText().trim().length()<1 ){
            JOptionPane.showMessageDialog(null, "Caractere valor Insuficiente(<1)", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(jtDetalhamentodespesa.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Detalhamento Vazio", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
