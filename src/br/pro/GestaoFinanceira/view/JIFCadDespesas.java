    private boolean preencherObjeto() throws Exception{
        obj.setIddespesa(Integer.parseInt(jlIddespesa1.getText()));
        obj.setDatadespesa(jtDatadespesa.getText());
        obj.setValordespesa(Float.parseFloat(jtValordespesa.getText()));
        obj.setDetalhamentodespesa(jtDetalhamentodespesa.getText());
        obj.setEmpresa(listaEmpresa.get(jcbEmpresa.getSelectedIndex()));
        
    return true;    
    }
