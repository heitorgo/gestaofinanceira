CREATE TABLE USUARIO(
    ID_USUARIO SERIAL NOT NULL,
    LOGIN VARCHAR(60)NOT NULL,
    SENHA VARCHAR(60)NOT NULL,
    CONSTRAINT UN_LOGIN UNIQUE(LOGIN),
    CONSTRAINT UN_SENHA UNIQUE(SENHA),
    CONSTRAINT PK_USUARIO PRIMARY KEY(ID_USUARIO)
);

CREATE TABLE CLIENTE(
    ID_CLIENTE SERIAL NOT NULL,
    NOME_CLIENTE VARCHAR(60) NOT NULL,
    DTA_NASC_CLIENTE VARCHAR(40) NOT NULL,
    EMAIL_CLIENTE VARCHAR(60),
    CPF_CLIENTE  VARCHAR(20) NOT NULL,
    CIDADE_CLIENTE VARCHAR(60),
    RUA_CLIENTE VARCHAR(60),
    BAIRRO_CLIENTE VARCHAR(60),
    NUMCASA_CLIENTE VARCHAR(20),
    CONTATO_CLIENTE VARCHAR(60),
    COMPL_CLIENTE VARCHAR(60),
    ID_EMPRESA BIGINT,
    CONSTRAINT PK_CLIENTE PRIMARY KEY (ID_CLIENTE),
    CONSTRAINT FK_EMPRESA FOREIGN KEY(ID_EMPRESA) REFERENCES EMPRESA,
    CONSTRAINT UN_CPF UNIQUE (CPF_CLIENTE)
);

